import time

a = 60   # 60 = 0011 1100
b = "60"
c = 0b1011  # 二进制数

# 左移运算符
a = a << 2  # 240 = 1111 0000

# 右移运算符
a = a >> 2  # 15 = 0000 1111

"""
类型转换
"""
# str -> int  字符串 -> 10进制
b1 = int(b)  # 60

# int -> bin  10进制 -> 2进制
a1 = bin(a)  # 0b111100

# bin -> int  2进制 -> 10进制
a2 = int(a1, 2)  # 60

# int -> oct  10进制 -> 8进制
a3 = oct(a)  # 0o74

# int -> hex  10进制 -> 16进制
a4 = hex(a)  # 0x3c
